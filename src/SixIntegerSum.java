public class SixIntegerSum { // należy pamiętać o nazywaniu plików i class tak samo
    public static void main(String[] args) {
        int number1 = 11;                               //number1 do 5 to deklaracje zmiennych znak = przypisuje do zmiennej wartość
        int number2 = 22;
        int number3 = 33, number4 = 44, number5 = 55, number6 = 66;   // zmienne można równiez deklarować w ten sposób - optymalizowanie kodu;
        int sum;                                        //wprowadzenie zmiennej bez wartości początkowej

        sum = number1 + number2 + number3 + number4 + number5 + number6; //działanie matematyczne, przypisujące jednocześnie wartość
        System.out.print("The sum is "); //wyświetlenie zdania
        System.out.println(sum); //wyświetlenie wyniku działania na zmiennych


    }
}
