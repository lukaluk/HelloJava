public class Print {
    public static void main(String[] args) {
       System.out.println("Hello Java");            //wyświetla zdanie i przenosi kursor do następnej linii
       System.out.println("Hello, Hello Luka");     //tutaj podopbnie jak linijkę wyżej
        System.out.println();                       //tutaj wstawia pustą linię i przenosi kursor do następnej linijki
        System.out.print("Hello You  ");            //tutaj wyświetla napis i kursor zostaje na końcu linii
        System.out.print("mothafocker!");           //tutaj dokładnie jak wyżej
        System.out.println();                       //tutaj wstawia pustą linię
        System.out.print("Coś takiego, ");          //tutaj wyświetla napis i kursor zostaje na końcu lini
         System.out.print("    !     ");            //j.w.
         System.out.println("   właśnie tak!");     //tu kończy wyświetlać i przenosi się na początek następnej linii
        System.out.println("uff, koniec");          //j.w.
    }
}
